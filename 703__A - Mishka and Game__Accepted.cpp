#include<bits/stdc++.h>
using namespace std;
int main()
{
    int n,ms=0,ch=0;
    cin>>n;
    while(n--)
    {
        int x,y;
        cin>>x>>y;
        if(x>y)ms++;
        else if(y>x)ch++;
    }
    if(ms==ch)printf("Friendship is magic!^^\n");
    else if(ms>ch)
        printf("Mishka\n");
    else
        printf("Chris\n");
}#include<bits/stdc++.h>
